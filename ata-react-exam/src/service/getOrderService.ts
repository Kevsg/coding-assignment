export const getOrder = (
  startDateTime: Dayjs | undefined,
  endDateTime: Dayjs | undefined
) => {
  const start = startDateTime?.toDate();
  const end = endDateTime?.toDate();

  if (!start || !end) {
    return mockOrders;
  }
  return mockOrders.filter((order) => {
    const ord = new Date(order.createdAt);
    return start < ord && end > ord;
  });
};
